#include "string.h"

size_t	ft_countwords(char *str, char sep)
{
	size_t sum;
	size_t i;

	i = 0;
	sum = 0;
	while (str[i] && str[i] == sep)
		i++;
	while (str[i])
	{
		while (str[i] && str[i] != sep)
			i++;
		sum++;
		while (str[i] && str[i] == sep)
			i++;
	}
	return (sum);
}
